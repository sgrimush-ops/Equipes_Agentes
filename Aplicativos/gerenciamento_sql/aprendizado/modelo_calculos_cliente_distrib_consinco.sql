    VLRFRETE, 
    VLRVERBAVDA, 
    VLRVERBASELLIN, 
    VLRVERBAPDV, 
    RECOMPOSICAOMARGEM,
    
    -- Margem de Contribuição, Fiscal e Margens Estendidas
    VLRCONTRIB, 
    MARGEMCONTRIB, 
    MARGEMCONTRIBSVMI, 
    VLRVENDAPORM3, 
