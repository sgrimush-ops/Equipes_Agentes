    VLRDESPFIXA, VLRDESCFIXO, 
    
    -- Campanhas Dinâmicas / DOTZ
    QTDDOTZNORMAL, VLRVENDADOTZNORMAL, 
    QTDDOTZEXTRA, VLRVENDADOTZEXTRA, 
    QTDDOTZTOTAL, VLRVENDADOTZTOTAL, 
    VLRDIFCAMPANHAPRECO, VLRDIFCAMPANHAPRECODEVOL, 
